def caesar(message, cypher, type):
    if cypher > 26:
            cypher = cypher%26    
    msg = ""
    for x in message:
        if x in alphabet:
            cypher_index = alphabet.index(x)
            if type == "encode":
                cypher_index += cypher
            elif type == "decode":
                cypher_index -= cypher
            cypher_index = cypher_index % 26
            msg += alphabet[cypher_index]
        else:
            msg += x
    print(msg)
        


cont = True

